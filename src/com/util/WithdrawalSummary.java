package com.util;

public class WithdrawalSummary {

	private String ssn;
	private String withdrawalAmount;
	private String withdrawalDate;
	private String additionalDividend;
	private String employeeId;
	
	public String getSsn() {
		return ssn;
	}
	public void setSsn(String ssn) {
		this.ssn = ssn;
	}
	public String getWithdrawalAmount() {
		return withdrawalAmount;
	}
	public void setWithdrawalAmount(String withdrawalAmount) {
		this.withdrawalAmount = withdrawalAmount;
	}
	public String getWithdrawalDate() {
		return withdrawalDate;
	}
	public void setWithdrawalDate(String withdrawalDate) {
		this.withdrawalDate = withdrawalDate;
	}
	public String getAdditionalDividend() {
		return additionalDividend;
	}
	public void setAdditionalDividend(String additionalDividend) {
		this.additionalDividend = additionalDividend;
	}
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}

	

}
