package com.util;

public class BalanceSummary {

	private String ssn;
	private String shareAmount;
	private String reserveAmount;
	private String balanceForward;
	private String totalShare;
	private String withdrawalAmount;
	private String dividend;
	private String balanceAfterDividend;
	private String periodMonth;
	private String periodYear;
	private String monthString;

	public String getSsn() {
		return ssn;
	}
	public void setSsn(String ssn) {
		this.ssn = ssn;
	}
	public String getShareAmount() {
		return shareAmount;
	}
	public void setShareAmount(String shareAmount) {
		this.shareAmount = shareAmount;
	}
	public String getReserveAmount() {
		return reserveAmount;
	}
	public void setReserveAmount(String reserveAmount) {
		this.reserveAmount = reserveAmount;
	}
	public String getBalanceForward() {
		return balanceForward;
	}
	public void setBalanceForward(String balanceForward) {
		this.balanceForward = balanceForward;
	}
	
	public String getTotalShare() {
		return totalShare;
	}
	public void setTotalShare(String totalShare) {
		this.totalShare = totalShare;
	}
	public String getWithdrawalAmount() {
		return withdrawalAmount;
	}
	public void setWithdrawalAmount(String withdrawalAmount) {
		this.withdrawalAmount = withdrawalAmount;
	}
	public String getDividend() {
		return dividend;
	}
	public void setDividend(String dividend) {
		this.dividend = dividend;
	}
	public String getBalanceAfterDividend() {
		return balanceAfterDividend;
	}
	public void setBalanceAfterDividend(String balanceAfterDividend) {
		this.balanceAfterDividend = balanceAfterDividend;
	}
	public String getPeriodMonth() {
		return periodMonth;
	}
	public void setPeriodMonth(String periodMonth) {
		this.periodMonth = periodMonth;
	}
	public String getPeriodYear() {
		return periodYear;
	}
	public void setPeriodYear(String periodYear) {
		this.periodYear = periodYear;
	}
	public String getMonthString() {
		return monthString;
	}
	public void setMonthString(String monthString) {
		this.monthString = monthString;
	}
	
	
	
}
